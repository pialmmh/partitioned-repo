package com.telcobright.splitverse.tests.entities;

import com.telcobright.core.annotation.*;
import com.telcobright.core.entity.ShardingEntity;
import java.math.BigDecimal;
import java.time.LocalDateTime;

@Table(name = "orders")
public class OrderEntity implements ShardingEntity<LocalDateTime> {

    @Id(autoGenerated = false)
    @Column(name = "id")
    private String id;

    @ShardingKey
    @Column(name = "order_date")
    private LocalDateTime orderDate;

    @Column(name = "customer_id")
    private String customerId;

    @Column(name = "total_amount")
    private BigDecimal totalAmount;

    @Column(name = "status")
    private String status;

    @Column(name = "shipping_address")
    private String shippingAddress;

    // Default constructor
    public OrderEntity() {}

    // Constructor with parameters
    public OrderEntity(String id, String customerId, BigDecimal totalAmount, LocalDateTime orderDate) {
        this.id = id;
        this.customerId = customerId;
        this.totalAmount = totalAmount;
        this.orderDate = orderDate;
        this.status = "pending";
    }

    @Override
    public String getId() {
        return id;
    }

    @Override
    public void setId(String id) {
        this.id = id;
    }

    @Override
    public LocalDateTime getPartitionColValue() {
        return orderDate;
    }

    @Override
    public void setPartitionColValue(LocalDateTime value) {
        this.orderDate = value;
    }

    // Getters and setters
    public LocalDateTime getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(LocalDateTime orderDate) {
        this.orderDate = orderDate;
    }

    public String getCustomerId() {
        return customerId;
    }

    public void setCustomerId(String customerId) {
        this.customerId = customerId;
    }

    public BigDecimal getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(BigDecimal totalAmount) {
        this.totalAmount = totalAmount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getShippingAddress() {
        return shippingAddress;
    }

    public void setShippingAddress(String shippingAddress) {
        this.shippingAddress = shippingAddress;
    }
}