package com.telcobright.examples.entity;

import com.telcobright.core.entity.ShardingEntity;

import com.telcobright.core.annotation.Column;
import com.telcobright.core.annotation.Id;
import com.telcobright.core.annotation.ShardingKey;
import com.telcobright.core.annotation.Table;

import java.time.LocalDateTime;


@Table(name = "student")
public class Student implements ShardingEntity<LocalDateTime> {
    @Id(autoGenerated = false)  // Externally generated ID
    @Column(name = "id")
    private String id;

    @Column(name = "first_name", nullable = false)
    private String firstName;

    @Column(name = "age")
    private Integer age;

    @Column(name = "email", nullable = false)
    private String email;

    @Column(name = "phone")
    private String phone;

    @ShardingKey
    @Column(name = "created_at", nullable = false)
    private LocalDateTime createdAt;

    public Student() {}
    
    public Student(String firstName, Integer age, String email, String phone, LocalDateTime createdAt) {
        this.firstName = firstName;
        this.age = age;
        this.email = email;
        this.phone = phone;
        this.createdAt = createdAt;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String firstName) {
        this.firstName = firstName;
    }

    public void setAge(Integer age) {
        this.age = age;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return firstName;
    }

    public Integer getAge() {
        return age;
    }

    public String getEmail() {
        return email;
    }

    public String getPhone() {
        return phone;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    // Implementation of ShardingEntity<LocalDateTime> methods
    @Override
    public LocalDateTime getPartitionColValue() {
        return createdAt;
    }

    @Override
    public void setPartitionColValue(LocalDateTime value) {
        this.createdAt = value;
    }
}
